package practiceproblequestion;
public class p11 {
    public static void main(String[] args) {
        //Power in java
        int number=2;
        int power=3;
        double result=Math.pow(number, power);
        //Math.pow is used to calculate the power of a number. It takes two arguments, the base and the exponent, and returns the result of base raised to the power of exponent.
        System.out.println(number+" raised to the power of "+power+" is: "+result);
    }
}
