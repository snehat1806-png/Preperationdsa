package practiceproblequestion;
public class p10 {
    //Calculate Commission Percentage
    public static void main(String[] args) {
        double sales=5000;
        double commissionRate=0.1; //10% commission
        double commission=sales*commissionRate;
        //Commission is calculated by multiplying the sales amount with the commission rate.
        //In this case, the commission will be 5000 * 0.1 = 500.
        //The commission is then printed to the console.
        System.out.println("Commission is: "+commission);
    }
}
